// Question 4: What is the purpose of declaring variables in JavaScript, and how do you declare them using the
// 'let' keyword?

// solution:

// - In JavaScript, declaring variables serves the purpose of allocating memory to store and manipulate data within a program. 
// - Variables allow developers to store values and reference them by a name, enabling flexibility and reusability in code.
// - The let keyword is used to declare variables in JavaScript. 
// - Here's a simple example:

let myVariable; // Declaration without initialization

myVariable = 42; // Initialization with a value

// - In this example, myVariable is declared using let. 
// - Initially, it doesn't have a value (undefined). Later, it is assigned the value 42. 