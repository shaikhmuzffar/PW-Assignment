// 1.Arithmetic Operators: Perform basic mathematical operations.

let a = 5;
let b = 2;

let sum = a + b; // Addition
let difference = a - b; // Subtraction
let product = a * b; // Multiplication
let quotient = a / b; // Division
let remainder = a % b; // Modulus (remainder)

// 2.Assignment Operators: Assign values to variables.

let x = 10;
x += 5; // Equivalent to x = x + 5

// 3.Comparison Operators: Compare values and return a Boolean result.

let isEqual = a === b; // Strict equality
let isNotEqual = a !== b; // Strict inequality
let isGreaterThan = a > b; // Greater than
let isLessThan = a < b; // Less than

// 4.Logical Operators: Combine or negate Boolean values.

let logicalAnd = (a > 0) && (b < 10); // Logical AND
let logicalOr = (a < 0) || (b > 0); // Logical OR
let logicalNot = !(a === b); // Logical NOT

// 5.Increment/Decrement Operators: Increase or decrease the value of a variable.

let counter = 0;
counter++; // Increment by 1
counter--; // Decrement by 1

// String Operators: Concatenate strings.

let firstName = "John";
let lastName = "Doe";
let fullName = firstName + " " + lastName; // String concatenation