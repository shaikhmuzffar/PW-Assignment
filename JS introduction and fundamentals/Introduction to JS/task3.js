//1. Unary Operators: Operate on a single operand.

let x = 5;
let y = -x; // Unary minus (negation)
let isTrue = true;
let isFalse = !isTrue; // Unary NOT


//2. Binary Operators: Operate on two operands.

let a = 10;
let b = 3;
let sum = a + b; // Addition
let difference = a - b; // Subtraction
let product = a * b; // Multiplication
let quotient = a / b; // Division

//3.Ternary Operator: Operates on three operands and is often used as a shorthand for an if-else statement.

let age = 20;
let isAdult = (age >= 18) ? "Yes" : "No"; // Ternary conditional operator